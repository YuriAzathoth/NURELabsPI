<p>По размерам, географическим показателям и химическому составу планеты подразделяются на две группы: <b>планеты земной группы</b> и <b>планеты-гиганты</b>. К планетам земной группы относятся <a href='mercury.php'>Меркурий</a>, <a href='venus.php'>Венера</a>, Земля и <a href='mars.php'>Марс</a>. О них и пойдет речь в этом подразделе.</p>

