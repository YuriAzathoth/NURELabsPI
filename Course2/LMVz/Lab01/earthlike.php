<p><b>Земля</b> — третья планета от Солнца. Ей будет посвящен отдельный подраздел.</p></p>
<p><b>Давайте обобщим</b>. От местоположения планеты в Солнечной системе зависит плотность вещества планеты, а с учетом ее размеров — и масса. Чем ближе планета к Солнцу, тем выше у нее средняя плотность вещества. Например, у Меркурия она составляет 5,42 г/см\ Венеры — 5,25, Земли — 5,25, Марса — 3,97 г/см3.</p>
<p>Общими характеристиками планет земной группы (Меркурий, Венера, Земля, Марс) являются прежде всего: 1) сравнительно небольшие размеры; 2) высокие температуры на поверхности и 3) высокая плотность вещества планет. Эти планеты сравнительно медленно вращаются вокруг своей оси и имеют мало спутников или не имеют их совсем. В строении планет земной группы выделяют четыре главные оболочки: 1) плотное ядро; 2) покрывающую его мантию; 3) кору; 4) легкую газо- во-водную оболочку (исключая Меркурий). На поверхности этих планет обнаружены следы тектонической деятельности.</p>
