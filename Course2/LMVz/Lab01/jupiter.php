<p><h2>Атмосфера Юпитера</h2></p>
<p>Атмосфера Юпитера очень плотная. Она состоит из водорода (89 %) и гелия (11 %), напоминая по химическому составу Солнце (рис. 1). Ее протяженность 6000 км. Оранжевый цвет атмосфере придают соединения фосфора или серы. Для людей она губительна, так как содержит ядовитые аммиак и ацетилен.</p>
<p>Разные части атмосферы планеты вращаются с разными скоростями. Такое различие породило пояса облаков, которых у Юпитера три: наверху — облака из оледеневшего аммиака; под ними — кристаллы сероводорода аммония и метана, а в самом нижнем слое — водяной лед и, возможно, жидкая вода. Температура верхних облаков составляет 130 °С. Кроме того, Юпитер имеет водородную и гелиевую короны. Ветры на Юпитере достигают скорости 500 км/ч.</p>
<p>Достопримечательностью Юпитера является Большое Красное Пятно, которое наблюдают уже 300 лет. Оно было открыто в 1664 г. английским естествоиспытателем <b>Робертом Гуком</b> (1635-1703). Сейчас его длина достигает 25 000 км, а 100 лет назад она была около 50 000 км. Это пятно впервые было описано в 1878 г., а зарисовано 300 лет назад. Оно как бы живет своей жизнью — то расширяется, то сжимается. Цвет его также меняется.</p>
<p>Американские зонды «Пионер-10» и «Пионер-11», «Вояджер-1» и «Вояджер-2», «Галилео» выяснили, что у пятна нет твердой поверхности, оно вращается, как циклон в атмосфере Земли. Предполагают, что Большое Красное Пятно — это атмосферное явление, вероятно, верхушка циклона, бушующего в атмосфере Юпитера. В атмосфере Юпитера обнаружено также белое пятно размером более 10 000 км.</p>
<p>На 1 марта 2009 г. у Юпитера известно 63 спутника. Самые крупные из них Но и Европа размером с Меркурий. Они всегда повернуты к Юпитеру одной стороной, как Луна к Земле. Эти спутники называют галилеевыми, так как их впервые открыл итальянский физик, механик и астроном <b>Галилео Галилей</b> (1564-1642) в 1610 г., испытывая свой телескоп. На Ио имеются действующие вулканы.</p>

