<p>Период вращения Юпитера вокруг оси — 9 ч 55 мин. При этом каждая точка экватора движется со скоростью 45 000 км/ч.</p>
<p>Так как Юпитер — не твердый шар, а состоит из газа и жидкости, экваториальные его части вращаются быстрее, чем приполярные области. Ось вращения Юпитера почти перпендикулярна его орбите, следовательно, на планете смена времен года выражена слабо.</p>
<p>Масса Юпитера намного превышает массу всех других планет Солнечной системы, вместе взятых, и составляет 1,9 • 10<sup>27</sup> кг. При этом средняя плотность Юпитера составляет 0,24 средней плотности Земли.</p>

