<p>Планета Сатурн — один из самых ярких объектов на нашем звездном небе. Отличительной ее особенностью является наличие колец. Впервые их увидел в 1610 г. Г. Галилей, но не понял, что это такое, записав, что Сатурн состоит из частей.</p>
<p>Полвека спустя голландский математик, физик и астроном <b>Христиан Гюйгенс</b> (1629-1695) сообщил о наличии у Сатурна кольца, а в 1675 г. известный итальянский и французский астроном <b>Жан Доминик Кассини</b> (1625-1712) обнаружил между кольцами щель.</p>
<p>Эти кольца видимы с Земли даже в небольшой телескоп. Они состоят из тысяч и тысяч небольших твердых обломков камней и льда, которые вращаются вокруг планеты. Один раз в 14-15 лет колец Сатурна с Земли не видно, так как они поворачиваются ребром.</p>

