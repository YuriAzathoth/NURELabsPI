<p>Поэтому Сатурн — не твердый шар, а состоит из газа и жидкости, экваториальные его части быстрее вращаются, чем приполярные области: на полюсах один оборот совершается примерно на 26 мин медленнее.</p>
<p>Одна из особенностей Сатурна заключается в том, что это единственная планета в Солнечной системе, чья плотность меньше плотности воды. Атмосфера Сатурна очень плотная, она состоит на 94 % из водорода и на 6 % из гелия. Температура на поверхности планеты 150 °С.</p>
<p>Скорость ветров на Сатурне зависит от широты места, достигая 500 м/с, что втрое больше, чем на Юпитере. В атмосфере Сатурна часто наблюдаются штормы, хотя и не такие мощные, как знаменитое Красное Пятно Юпитера. В частности, на Сатурне обнаружено Большое Коричневое Пятно.</p>
<p>Планета имеет восемь больших главных и множество малых спутников.</p>
<p>Большинство спутников состоит из льда: их плотность не превышает 1400 кг/м3 У наиболее крупных спутников формируется каменистое ядро. Почти все спутники всегда повернуты к планете одной стороной.</p>
<p>Самый крупный спутник Сатурна — Титан. По своей величине он превосходит планету Меркурий. Его диаметр 5150 км. Он был открыт в 1655 г. Христианом Гюйгенсом. На Титане есть океаны, моря, континенты. Температура составляет 180 °С. Этот спутник окутан оранжевой атмосферой из метана и этана.</p>
<p>Спутник Энцелад — самое светлое тело Солнечной системы, которое, по-видимому, покрыто тонким слоем инея. Два наиболее крупных кратера на этом спутнике Сатурна носят имена Али-Бабы и Аладдина.</p>
<p>Гиперион — темный спутник неправильной формы с хаотическим собственным вращением. Он не имеет постоянной скорости вращения вокруг своей оси: она меняется в течение месяца на десятки процентов.</p>
<p>Спутник Сатурна Феба обращается вокруг планеты в обратную сторону.</p>

