<p>Благодаря советским и американским межпланетным станциям в настоящее время известно, что Венера — планета со сложным рельефом.</p>
<p>Здесь обнаружены гористые участки местности с перепадом высот 2-3 км, вулкан с диаметром основания 300-400 км и высотой около 1 км, огромная котловина (протяженностью 1500 км с севера на юг и 1000 км с запада на восток) и относительно ровные участки. В приэкваториальной области планеты имеется более 10 кольцевых структур, подобных кратерам Меркурия, диаметром от 35 до 150 км, но сильно сглаженных и плоских. Кроме этого, в коре планеты есть разлом длиной 1500 км, шириной 150 км и глубиной около 2 км.</p>
<p>В 1981 г. станции «Венера-13» и «Венера-14» исследовали образцы грунта планеты и передали на землю первые цветные фотографии Венеры. Благодаря этому мы знаем, что поверхностные породы планеты близки по составу к земным осадочным породам, а небо над горизонтом Венеры оранжево-желто-зеленое.</p>
<p>В настоящее время полеты людей на Венеру маловероятны, но на высоте 50 км от планеты температура и давление близки к условиям на Земле, поэтому здесь возможно создание межпланетных станций для изучения Венеры и для подзарядки космических кораблей.</p>

