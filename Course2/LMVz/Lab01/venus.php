<p>Планета Венера — наша ближайшая соседка. Венера подходит к Земле ближе, чем любая другая планета, на расстояние 40 млн км и ближе. Расстояние от Солнца до Венеры составляет 108 000 000 км, или 0,723 а.е.</p> 
<p>Размеры Венеры и масса близки к земным: диаметр планеты всего на 5 % меньше диаметра Земли, масса — 0,815 массы Земли, сила тяжести — 0,91 земной. При этом Венера очень медленно вращается вокруг своей оси в направлении, обратном вращению Земли (т. е. с востока на запад).</p>
<p>Несмотря на то, что в XVII-XVIII вв. различные астрономы неоднократно сообщали об открытии естественных спутников Венеры. В настоящее время известно, что таковые у планеты отсутствуют.</p>

<p><h2>Атмосфера Венеры</h2></p>
<p>В отличие от других планет земной группы, изучение Венеры с помощью телескопов оказалось невозможным, так как еще <b>М. В. Ломоносов (1711 — 1765)</b>, наблюдая 6 июня 1761 г. прохождение планеты на фоне Солнца, установил, что Венера окружена «знатною воздушною атмосферой, таковой (лишь бы не большею), какова обливается около нашего шара земного».</p>
<p>Атмосфера планеты простирается до высоты <b>5500</b> км, а ее плотность в <b>35</b> раз превосходит плотность земной. Атмосферное давление в <b>100</b> раз выше чем на Земле, и достигает 10 млн Па. Строение атмосферы этой планеты представлено на рис. 1.</p>
<p>Последний раз прохождение Венеры на фоне диска Солнца в России астрономы, ученые и любители, смогли наблюдать 8 июня 2004 г. А 6 июня 2012 г. (т. е. с 8-летним интервалом) этот удивительный феномен можно будет наблюдать снова. Последующее прохождение состоится только через 100 лет.</p>

