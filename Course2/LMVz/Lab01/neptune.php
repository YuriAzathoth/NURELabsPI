<p>Нептун открыли по теоретическим расчетам. Дело в том, что Уран отклоняется от расчетной орбиты, будто его притягивает другая планета.</p>
<p>Британские математики и астрономы <b>Джон Кауч Адаме</b> (1819-1892) и Джеймс Чаллис в 1845 г. сделали расчет примерного места расположения планеты. В это же время французский астроном <b>Урбан Леверье</b> (1811 — 1877), сделав расчет, убедил начать поиск новой планеты. Впервые Нептун увидели астрономы 23 сентября 1846 г. недалеко от тех положений, которые независимо друг от друга предсказывали англичанин Адаме и француз Леверье.</p>
<p>Нептун значительно удален от Солнца.</p>

