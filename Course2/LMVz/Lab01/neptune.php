<p>Масса планеты в 17 раз больше массы Земли. Радиус планеты составляет около четырех земных радиусов. Плотность — Уз плотности Земли.</p>
<p>Вокруг Нептуна обнаружены кольца. Они разомкнуты (разорваны), т. е. состоят из отдельных арок, не связанных между собой. По виду кольца Урана и Нептуна похожи.</p>
<p>Строение Нептуна, вероятно, почти такое же, как Урана.</p>
<p>В отличие от <a href='jupiter.php'>Юпитера</a> с <a href='saturn.php'>Сатурном</a>, <a href='uran.php'>Уран</a> и Нептун, возможно, не имеют четкого внутреннего расслоения. Но, скорее всего, у Нептуна есть небольшое твердое ядро, равное по массе Земле. Атмосфера Нептуна — это, по большей части, водород и гелий с небольшой примесью метана (1 %). Синий цвет Нептуна является результатом поглощения красного света в атмосфере этим газом — как и на Уране.</p>
<p>Планета имеет грозовую атмосферу, тонкие пористые облака, состоящие из замерзшего метана. Температура атмосферы Нептуна выше, чем у Урана, следовательно, около 80% Н2</p>

