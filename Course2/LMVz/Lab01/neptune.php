<p>Нептун имеет собственный внутренний источник тепла — он излучает в 2,7 раза больше энергии, нежели получает от Солнца. Средняя температура поверхности планеты — 235 °С. На Нептуне наблюдаются сильнейшие ветры, параллельные экватору планеты, большие бури и вихри. На планете самые быстрые в Солнечной системе ветры, достигающие 700 км/ч. Ветры дуют на Нептуне в западном направлении, против вращения планеты.</p>
<p>На поверхности расположены горные хребты, трещины. Зимой идет азотный снег, а летом через трещины пробиваются фонтаны.</p>
<p>Зонд «Воялжер-2» обнаружил на Нептуне мощные циклоны, в которых скорость ветра достигает скорости звука.</p>
<p>Спутники планеты носят названия Тритон, Нереида, Наяда, Таласса, Протей, Деспина, Галатея, Ларисса. В 2002-2005 гг. были открыты еще пять спутников Нептуна. Каждый из новооткрытых имеет диаметр 30-60 км.</p>
<p>Самый большой спутник Нептуна — Тритон. Он был открыт в 1846 г. Уильямом Ласссллом. По размерам Тритон превосходит Луну. В Тритоне сосредоточена почти вся масса спутниковой системы Нептуна. Отличается большой плотностью: 2 г/см<sup>3</sup>.</p>

