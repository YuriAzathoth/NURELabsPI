<p>В настоящее время на Марсе не найдено ни одного действующего вулкана. Около Олимпа есть и другие гигантские вулканы: гора Аскрийская, гора Павлина и гора Арсия, высота которых превышает 20 км. Вытекшая из них лава, прежде чем застыть, растеклась во все стороны, поэтому вулканы по форме напоминают скорее лепешки, чем конусы. Есть на Марсе и песчаные дюны, гигантские каньоны и разломы, а также метеоритные кратеры. Наиболее грандиозная система каньонов — долина Маринера длиной 4 тыс. км. В прошлом на Марсе могли протекать реки, которые и оставили русла, наблюдаемые в настоящее время.</p>
<p>В 1965 г. американский зонд «Маринер-4» передал первые изображения Марса. На основании этих, а также снимков с «Маринер-9», советских зондов «Марс-4» и «Марс-5» и американских «Викинг-1» и «Викинг-2», работавших в 1974 г., была составлена первая карта Марса. А в 1997 г. американский космический корабль доставил на Марс робота — шестиколесную тележку длиной 30 см и массой 11 кг. Робот находился на Марсе с 4 июля по 27 сентября 1997 г., изучая эту планету. Передачи о его передвижении транслировались по телевидению и сети Интернет.</p>
<p>У Марса два спутника — Деймос и Фобос.</p>
<p>Предположение о существовании у Марса двух спутников высказал в 1610 г. немецкий математик, астроном, физик и астролог <b>Иоганн Кеплер</b> (1571 — 1630), открывший законы движения планет.</p>
<p>Однако открыты спутники Марса были только в 1877 г. американским астрологом <b>Асафом Холлом</b> (1829-1907).</p>

