<p>Орбита Марса вытянута, поэтому расстояние до Солнца меняется в течение года на 21 млн км. Расстояние до Земли также не постоянно. В Великие противостояния планет, происходящие один раз в 15-17 лет, когда Солнце, Земля и Марс выстраиваются в одну линию, Марс максимально приближается к Земле на 50-60 млн км. Последнее Великое противостояние было в 2003 г. Максимальная удаленность Марса от Земли достигает 400 млн км.</p>
<p>Год на Марсе почти вдвое длиннее земного — 687 земных дней. Ось наклонена к орбите — 65°, что ведет к смене времен года. Период вращения вокруг своей оси равен 24,62 ч, т. е. всего на 41 мин больше периода вращения Земли. Наклон экватора к орбите почти как у Земли. Это значит, что смена дня и ночи и смена времен года на Марсе протекает почти так же, как на Земле.</p>
<p>По расчетам, ядро Марса имеет массу до 9 % массы планеты. Оно состоит из железа и его сплавов и пребывает в жидком состоянии. Марс имеет мощную кору толщиной 100 км. Между ними находится силикатная мантия, обогащенная железом. Красный цвет Марса как раз и объясняется тем, что его грунт наполовину состоит из окислов железа. Планета как бы «проржавела».</p>
<p>Небо над Марсом темно-фиолетовое, и яркие звезды видны даже днем в спокойную тихую погоду. Атмосфера имеет следующий состав (рис. 46): углекислый газ — 95 %, азот — 2,5, атомарный водород, аргон — 1,6 %, остальное — водяные пары, кислород. Зимой углекислота замерзает, превращаясь в сухой лед. В атмосфере встречаются редкие облака, над низинами и на дне кратеров в холодное время суток стоят туманы.</p>

