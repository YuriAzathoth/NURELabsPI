<p>Среднее давление атмосферы на уровне поверхности около 6,1 мбар. Это в 15 000 раз меньше, чем на <a href='venus.php'>Венере</a>, и в 160 раз меньше, чем у поверхности Земли. В самых глубоких впадинах давление достигает 12 мбар. Атмосфера Марса сильно разряжена. Марс — холодная планета. Самая низкая зарегистрированная температура Марса -139 °С. Для планеты характерен резкий перепад температур. Амплитуда температур может составлять 75-60 °С. На Марсе есть климатические пояса, подобные земным. В экваториальном поясе в полдень температура поднимается до +20-25 °С, а ночью падает до -40 °С. В умеренном поясе утром температура составляет 50-80 °С.</p>
<p>Предполагают, что несколько миллиардов лет назад на Марсе была атмосфера плотностью 1-3 бар. При таком давлении вода должна находиться в жидком состоянии, а углекислый газ — испаряться, и мог возникнуть парниковый эффект (как на Венере). Однако Марс постепенно терял атмосферу из-за своей малой массы. Парниковый эффект уменьшался, появились вечная мерзлота и полярные шапки, которые наблюдаются и поныне.</p>
<p>На Марсе находится самый высокий вулкан Солнечной системы — Олимп. Его высота 27 400 м, а диаметр основания вулкана достигает 600 км. Это потухший вулкан, который, вероятнее всего, около 1,5 млрд лет назад извергал лаву.</p>

