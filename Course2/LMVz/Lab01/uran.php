<p>В отличие от других планет-гигантов ось вращения Урана находится почти в плоскости орбиты, т. е. наклон экватора к орбите составляет 82°. Уран как бы «лежит на боку», поэтому длительность полярного дня и ночи по широте, которые составляют на полюсах по 42 года, на широте 60° — 28 лет, на широте 30° — 14 лет.</p>
<p>Уран имеет небольшое твердое железно-каменное ядро, над которым сразу начинается плотная мощная, толщиной не менее 8000 км атмосфера. Она состоит на 83% из водорода, 15% гелия и 2% метана (рис. 1).</p>

