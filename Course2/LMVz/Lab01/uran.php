<p>Метан, ацетилен и другие углеводороды в атмосфере Урана встречаются в значительно больших количествах, чем на Юпитере и Сатурне. Именно метановая дымка хорошо поглощает красные лучи, поэтому Уран кажется голубым. Подобно другим газовым планетам, он имеет полосы облаков, которые очень быстро перемещаются.</p>
<p>Средняя температура на поверхности планеты — 200 °С. Зима и лето на Уране сильно разнятся: целое полушарие зимой на несколько лет прячется от Солнца. Летом там тоже не бывает жарко, так как Уран получает в 370 раз меньше тепла от Солнца,чем Земля. Ветры в средних широтах на Уране перемешают облака в тех же направлениях, что и на Земле. Они дуют со скоростью от 40 до 160 м/с (на Земле — около 50 м/с).</p>

