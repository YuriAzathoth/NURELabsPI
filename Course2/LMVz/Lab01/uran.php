<p>Уран был открыт 13 марта 1791 г. английским астрономом немецкого происхождения <b>Уильямом Гер шел ем</b> (1738-1822) (рис. 55). В 1787 г. он же открыл первые два спутника и дал им названия Оберон и Титания в честь короля и королевы фей из пьесы У. Шекспира «Сон в летнюю ночь». Это положило начало традиции называть новые спутники в честь персонажей пьес У. Шекспира: Дездемона, Корделия, Офелия, Джульетта, Розалинда, Белинда, Калибан и т. д. Наиболее крупный из них Титания, диаметром 1580 км. Всего же у Урана более 20 спутников.</p>
<p>В 1977 г. с Земли у Урана были открыты кольца, затем это открытие подтвердилось фотографиями с зонда «Вояджер-2», пролетевшего 24 января 1986 г. вблизи Урана.</p>

